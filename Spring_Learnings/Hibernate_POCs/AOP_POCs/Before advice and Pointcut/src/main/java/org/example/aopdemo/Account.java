package org.example.aopdemo;

public class Account {
    private String name;
    private String level;

    // Getter for name
    public String getName() {
        return name;
    }

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }

    // Getter for level
    public String getLevel() {
        return level;
    }

    // Setter for level
    public void setLevel(String level) {
        this.level = level;
    }

}
